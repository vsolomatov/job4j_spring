package ru.job4j.di;

public class StartUIContext {

    private Store store;
    private ConsoleInput consoleInput;

    public StartUIContext(Store store, ConsoleInput consoleInput) {
        this.store = store;
        this.consoleInput = consoleInput;
    }

    public void add(String value) {
        store.add(value);
    }

    public void print() {
        for (String value : store.getAll()) {
            System.out.println(value);
        }
    }
}